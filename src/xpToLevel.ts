export const xpToLevel = (xp: number): number => {
  if (xp < 100) return 0;
  const levelXp = levels.reduce((prev, value) => {
    if (xp < value && xp >= prev) return value;
    return prev;
  }, 0);
  if (!levelXp) return 100;
  return levels.indexOf(levelXp);
};
